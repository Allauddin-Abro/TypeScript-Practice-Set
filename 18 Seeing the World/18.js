// List of Famous Places
var places = ["Dubai", "America", "Canada", "Thailand", "Finland"];
// List in actual order
console.log(places);
// List in Alphabatical order without modifying original
var alphaSort = places.slice();
console.log(alphaSort.sort());
console.log(places);
// Reverse Alphabatical Order without modifying original
console.log(alphaSort.reverse());
console.log(places);
// Reverse order of original list
console.log(places.reverse());
console.log(places.reverse());
// Sort original list
console.log(places.sort());
//Reverse original in reverse alphbatical order
console.log(places.reverse());
